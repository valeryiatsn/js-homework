function capitalizeWords(str) {
  // —Ä–∞–∑–±–∏–≤–∞–µ—Ç —Å—Ç—Ä–æ–∫—É –Ω–∞ –º–∞—Å—Å–∏–≤ —Å–ª–æ–≤
  const words = str.split(' ');

  // map –¥–ª—è –ø—Ä–µ–æ–±—Ä–∞–∑–æ–≤–∞–Ω–∏—è –∫–∞–∂–¥–æ–≥–æ —Å–ª–æ–≤–∞
  const capitalizedWords = words.map(word => {
      // –ø–µ—Ä–≤–∞—è –±—É–∫–≤–∞ –∑–∞–≥–ª–∞–≤–Ω–æ–π –∏ –¥–æ–±–∞–≤–ª—è–µ—Ç –æ—Å—Ç–∞–ª—å–Ω—ã–µ –±—É–∫–≤—ã
      return word.charAt(0).toUpperCase() + word.slice(1);
  });

  // –æ–±—ä–µ–¥–∏–Ω—è–µ—Ç –º–∞—Å—Å–∏–≤ –æ–±—Ä–∞—Ç–Ω–æ –≤ —Å—Ç—Ä–æ–∫—É
  return capitalizedWords.join(' ');
}

console.log(capitalizeWords("hello world from javascript")); // –≤—ã–≤–æ–¥: "Hello World From Javascript"